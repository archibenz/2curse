% Практическая работа 5, вариант 10

% === Задание 1 ===
A = [2 6 3 5;
     4 2 6 7;
     1 4 7 3];

[m1, n1] = size(A);

disp('=== Задание 1 ===')
disp('Платежная матрица:');
disp(A);

min_rows_1 = min(A, [], 2);
disp('Минимальные по строкам:');
disp(min_rows_1);

alpha1 = max(min_rows_1);
disp(['Максимин (нижняя цена игры): ', num2str(alpha1)]);

max_cols_1 = max(A, [], 1);
disp('Максимальные по столбцам:');
disp(max_cols_1);

beta1 = min(max_cols_1);
disp(['Минимакс (верхняя цена игры): ', num2str(beta1)]);

saddle_exists_1 = (alpha1 == beta1);

if saddle_exists_1
    [row_idx_1, col_idx_1] = find(A == alpha1);
    disp('Седловая точка существует.');
    disp(['Позиция седловой точки: строка ', num2str(row_idx_1'), ', столбец ', num2str(col_idx_1')]);
    disp(['Значение игры: ', num2str(alpha1)]);
else
    disp('Седловая точка отсутствует (игра не решается в чистых стратегиях).');
end

disp(' ');
disp(' ');

% === Задание 2 (вариант 10: Камень, ножницы, бумага) ===
disp('=== Задание 2 (вариант 10: Камень, ножницы, бумага) ===')
disp('Платежная матрица (1 — Камень, 2 — Ножницы, 3 — Бумага):')
A_rps = [0  1 -1;
        -1  0  1;
         1 -1  0];
disp(A_rps);

min_rows_2 = min(A_rps, [], 2);
disp('Минимальные по строкам:');
disp(min_rows_2);
alpha2 = max(min_rows_2);
disp(['Максимин (нижняя цена игры): ', num2str(alpha2)]);

max_cols_2 = max(A_rps, [], 1);
disp('Максимальные по столбцам:');
disp(max_cols_2);
beta2 = min(max_cols_2);
disp(['Минимакс (верхняя цена игры): ', num2str(beta2)]);

if alpha2 == beta2
    [row_idx_2, col_idx_2] = find(A_rps == alpha2);
    disp('Седловая точка существует.');
    disp(['Позиция седловой точки: строка ', num2str(row_idx_2'), ', столбец ', num2str(col_idx_2')]);
    disp(['Значение игры: ', num2str(alpha2)]);
else
    disp('Седловой точки нет — игра не решается в чистых стратегиях.');
    disp('Оптимальные стратегии: использовать равновероятный случайный выбор (смешанная стратегия).');
end